int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (((i * i) < nb) && (nb <= 2147395600))
		i++;
	if ((i * i) == nb)
		return (i);
	return (0);
}

#include <stdio.h>

int	main(void)
{
	printf("%d\n", ft_sqrt(2147395600));
}
